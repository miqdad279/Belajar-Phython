# Belajar For Loop

pelanggan = ["Atikah", "Amel", "Farah"]
pelanggan.append("Hasna")
pelanggan.append("Putri")

# For adalah untuk mengakses semua data yang ada didalam list
for nama in pelanggan:
    print("========================")
    print(f"Nama Pelanggan : {nama}")
    print("========================")