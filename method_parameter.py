# Belajar Method Parameter

# satu parameter
def say_hello(name):
    print(f"Hi {name}")

say_hello("Atikah")
say_hello("Amel")
say_hello("Farah")
say_hello("Hasna")

# dua parameter

def say_hi(first_name, last_name):
    print(f"Hi {first_name} {last_name}")

say_hi("Atikah", "Salma Hidayati")
say_hi("Amel", "Raihana")
say_hi("Farah", "Amani Hesa Hasana Purnawarman")
say_hi("Hasna", "Nazihah Ahmad")
